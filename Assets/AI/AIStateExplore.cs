//---------------------------------------------------------------------------------------------------------------------     
using System;
using UnityEngine;
//---------------------------------------------------------------------------------------------------------------------     


//---------------------------------------------------------------------------------------------------------------------     
public class AIStateExplore : AIState
{


//---------------------------------------------------------------------------------------------------------------------     
  public AIStateExplore (Goto go)
  {
//    DebugConsole.Assert(DebugChannel.AI, go != null, "need a goto to explore");

    m_goto = go;
  }
  

//---------------------------------------------------------------------------------------------------------------------     
  public override void Update(float deltaTime)
  {
    if (m_goto.GotoState == Goto.State.INACTIVE)
    {
      GotoRandomPosition();
    }
    else
    {
      m_goto.Update(deltaTime);
    }
  }
  

//---------------------------------------------------------------------------------------------------------------------     
  public override void Enter(StateArgs stateArgs)
  {
    GotoRandomPosition();
  }
  
//---------------------------------------------------------------------------------------------------------------------     
  public override void Exit()
  {
    m_goto.Stop();
  }

  //---------------------------------------------------------------------------------------------------------------------     
  private void GotoRandomPosition()
  {
    Vector3 randPos = new Vector3(UnityEngine.Random.Range(0, 10), 0, UnityEngine.Random.Range(0, 10));
    m_goto.Go(randPos, 0, null, null, null);
  }

//---------------------------------------------------------------------------------------------------------------------     
  private Goto m_goto;

//---------------------------------------------------------------------------------------------------------------------     
}
//---------------------------------------------------------------------------------------------------------------------     


