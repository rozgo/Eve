using UnityEngine;
using System.Collections;

//---------------------------------------------------------------------------------------------------------------------
//---------------------------------------------------------------------------------------------------------------------
public abstract class AIState : IState {
    //---------------------------------------------------------------------------------------------------------------------
    public AIState () {
    }
    //---------------------------------------------------------------------------------------------------------------------
    public virtual void Update ( float deltaTime ) {
    }
    //---------------------------------------------------------------------------------------------------------------------
    public virtual void RealtimeUpdate () {
    }
    //---------------------------------------------------------------------------------------------------------------------
    public virtual void Enter ( StateArgs stateArgs ) {
    }
    //---------------------------------------------------------------------------------------------------------------------
    public virtual void Exit () {
    }
    //---------------------------------------------------------------------------------------------------------------------
    public virtual bool IsReady () {
        return true;
    }
    //---------------------------------------------------------------------------------------------------------------------
    public virtual bool CallExitOnReEntry () {
        return true;
    }
    //---------------------------------------------------------------------------------------------------------------------
}
