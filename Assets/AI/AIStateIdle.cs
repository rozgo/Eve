//---------------------------------------------------------------------------------------------------------------------     
using System;
using UnityEngine;
using System.Collections.Generic;

//---------------------------------------------------------------------------------------------------------------------     
//---------------------------------------------------------------------------------------------------------------------     
public class AIStateIdle : AIState
{
  
  
  //---------------------------------------------------------------------------------------------------------------------     
  public AIStateIdle (List<Timer> timers)
  {
    m_idleTimers = timers;
  }
  //---------------------------------------------------------------------------------------------------------------------     
  public override void Update (float deltaTime)
  {
    if (m_idleTimers != null)
    {
      for (int i = 0; i < m_idleTimers.Count; ++i)
      {
        m_idleTimers[i].Update();
      }
    }
  }
  //---------------------------------------------------------------------------------------------------------------------     
  public override void Enter (StateArgs stateArgs)
  {
  }
  //---------------------------------------------------------------------------------------------------------------------     
  public override void Exit ()
  {
  }

  private List<Timer> m_idleTimers;
  //---------------------------------------------------------------------------------------------------------------------     
}
//---------------------------------------------------------------------------------------------------------------------     


